package com.warehousemanager.shapemanagement;

/** Enum representing different types of shapes in the warehouse management system. */
public enum ShapeType {
  /** Represents a circle shape. */
  ELLIPSE,
  /** Represents an arc shape. */
  ARC,
  /** Represents a polygon shape. */
  POLYGON,
  /** Represents a rectangle shape. */
  RECTANGLE,
  /** Represents a container of other shapes. */
  CONTAINER
}
